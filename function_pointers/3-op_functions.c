#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - add two ints
 * @a: int
 * @b: int
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two ints
 * @a: int
 * @b: int
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul: - multiplicate two ints
 * @a: int
 * @b: int
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two ints
 * @a: int
 * @b: int
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - get the mod of a two ints
 * @a: name of the person
 * @b: a void function
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
